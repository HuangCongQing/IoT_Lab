void McuInit(void)
{
	RCC_Configuration();     							 // clock config,打开相应外设时钟;
	LedGpioInit('B',4,5,8,9);							// LED灯初始化
	Sm_GPIOInit(4);										   // 将PA4配置为ADC1的通道4同时初始化485控制引脚
	SmAdcInit(4);           						// ADC采样初始化 
	UartInit(2,115200);								 // USART2初始化
}

